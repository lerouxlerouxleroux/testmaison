Samarcande (en ouzbek : Samarqand, en tadjik : Самарқанд, en russe : Самарканд, en persan : سمرقند, en turc : Semerkand), population 400&nbsp;000 habitants, est une ville d'Ouzbékistan, capitale de la province de Samarcande. Son nom signifie probablement « lieu de la rencontre » ou « lieu du conflit » (samara : rencontre, conflit en sanskrit, kand, kent : ville, terme centre-asiatique) et illustre bien sa position à la limite des mondes turc et persan. Riche en monuments historiques, Samarcande a été proclamée en 2001 par l'UNESCO carrefour de cultures et site du patrimoine mondial.
